# ======================== ФУНКЦИЯ ГЕНЕРАЦИИ ПОСТА ЧЕРЕЗ GROQ ========================
async def generate_post_groq(topic: str) -> str:
    if not groq_client:
        return None
    current_date = datetime.now().strftime("%d.%m.%Y")
    user_prompt = f"Тема поста: {topic}. Текущая дата: {current_date} (учти сезонность). Сгенерируй пост в стиле Автомастер24."
    for attempt in range(2):
        try:
            response = groq_client.chat.completions.create(
                model="llama3-70b-8192",
                messages=[
                    {"role": "system", "content": SYSTEM_PROMPT},
                    {"role": "user", "content": user_prompt}
                ],
                temperature=0.85,
                max_tokens=1500
            )
            post = response.choices[0].message.content.strip()
            if post:
                return post
        except Exception as e:
            logger.warning(f"Groq attempt {attempt+1} failed: {e}")
            await asyncio.sleep(3)
    return None

# ======================== РАБОТА С ФОТОГРАФИЯМИ ========================
PHOTO_DIR = Path("images")
PHOTO_DIR.mkdir(exist_ok=True)  # если папки нет — создадим

def get_photo_for_post(post_text: str) -> tuple[str | None, str]:
    """Ищет фото по маркеру [PHOTO_REQUIRED: ...], возвращает путь к файлу и очищенный текст."""
    match = re.search(r'\[PHOTO_REQUIRED:\s*(.*?)\]', post_text)
    if not match:
        return None, post_text
    keywords = match.group(1).lower()
    # Удаляем маркер из текста
    cleaned_text = re.sub(r'\[PHOTO_REQUIRED:.*?\]', '', post_text, flags=re.DOTALL)
    # Ищем файл в папке images, имя которого содержит ключевые слова
    if PHOTO_DIR.exists():
        for img in PHOTO_DIR.iterdir():
            if img.suffix.lower() in ('.jpg', '.jpeg', '.png', '.webp'):
                if keywords in img.stem.lower():
                    return str(img), cleaned_text
    # Если не нашли, но есть дефолтное фото "no_image.jpg" — можно вернуть его
    default_img = PHOTO_DIR / "no_image.jpg"
    if default_img.exists():
        return str(default_img), cleaned_text + "\n\n⚠️ Изображение не найдено."
    return None, cleaned_text

def send_photo_to_telegram(photo_path: str, caption: str) -> bool:
    url = f"https://api.telegram.org/bot{BOT_TOKEN}/sendPhoto"
    try:
        with open(photo_path, 'rb') as f:
            files = {'photo': f}
            data = {'chat_id': CHANNEL_ID, 'caption': caption, 'parse_mode': 'HTML'}
            resp = requests.post(url, files=files, data=data, timeout=30)
        return resp.status_code == 200
    except Exception as e:
        logger.error(f"Ошибка отправки фото: {e}")
        return False

def send_to_telegram(text: str) -> bool:
    """Отправка только текста (если фото нет)"""
    url = f"https://api.telegram.org/bot{BOT_TOKEN}/sendMessage"
    payload = {
        "chat_id": CHANNEL_ID,
        "text": text,
        "parse_mode": "HTML",
        "disable_web_page_preview": False
    }
    try:
        resp = requests.post(url, json=payload, timeout=30)
        return resp.status_code == 200
    except Exception as e:
        logger.error(f"Ошибка отправки текста: {e}")
        return False


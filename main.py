def get_fallback_text(day_type):
    """Возвращает случайный запасной текст для данного типа дня."""
    return random.choice(FALLBACK_TEXTS.get(day_type, FALLBACK_TEXTS["tuesday"]))

# ======================== ГЕНЕРАЦИЯ ПОСТА ЧЕРЕЗ GROQ (С ПОДСТАНОВКОЙ ССЫЛОК) ========================
async def generate_post(day_type: str) -> str:
    if not groq_client:
        return None
    prompt = PROMPTS.get(day_type, PROMPTS["tuesday"])
    prompt = prompt.format(bot_link=BOT_LINK, site_url=SITE_URL)
    try:
        response = groq_client.chat.completions.create(
            model="llama3-70b-8192",
            messages=[{"role": "user", "content": prompt}],
            temperature=0.85,
            max_tokens=800
        )
        post = response.choices[0].message.content.strip()
        # если нейросеть не добавила ссылку на бот – добавляем сами
        if BOT_LINK not in post:
            post += f"\n\n👉 Подробнее и помощь в боте: {BOT_LINK}"
        if SITE_URL not in post:
            post += f"\n👉 Сайт сервиса: {SITE_URL}"
        return post
    except Exception as e:
        logger.warning(f"Groq ошибка ({day_type}): {e}")
        return None

# ======================== ОСНОВНАЯ ЛОГИКА (выбор дня) ========================
def get_day_type():
    weekday = datetime.now().weekday()  # 0=пн, 6=вс
    if weekday == 0:
        return "monday"
    elif weekday == 1:
        return "tuesday"
    elif weekday == 2:
        return "wednesday"
    elif weekday == 3:
        return "thursday"
    elif weekday == 4:
        return "friday"
    else:   # суббота, воскресенье
        return "weekend"

def send_to_telegram(text: str) -> bool:
    url = f"https://api.telegram.org/bot{BOT_TOKEN}/sendMessage"
    payload = {
        "chat_id": CHANNEL_ID,
        "text": text,
        "parse_mode": "HTML",
        "disable_web_page_preview": False
    }
    try:
        resp = requests.post(url, json=payload, timeout=30)
        return resp.status_code == 200
    except Exception as e:
        logger.error(f"Ошибка отправки: {e}")
        return False

async def main():
    logger.info("=== АВТОМАСТЕР24 – ЕЖЕДНЕВНЫЙ ПОСТ ПО РАСПИСАНИЮ ===")
    day_type = get_day_type()
    logger.info(f"Тип дня: {day_type}")

    # Пытаемся сгенерировать через Groq (до 2 попыток)
    post_text = await generate_post(day_type)
    if not post_text:
        logger.warning("Groq недоступен, берём запасной пост")
        post_text = get_fallback_text(day_type)
        # добавим ссылки на бот, если их нет в запасном тексте
        if BOT_LINK not in post_text:
            post_text += f"\n\n👉 Перейти в бот: {BOT_LINK}"
        if SITE_URL not in post_text:
            post_text += f"\n👉 Сайт сервиса: {SITE_URL}"

    # Очистка от чужих телеграм-ссылок
    post_text = re.sub(r'(https?://)?t\.me/\S+', '', post_text)
    # гарантируем ссылку на бот в самом конце, если её нет
    if BOT_LINK not in post_text and "bot" not in post_text.lower():
        post_text += f"\n\n👉 Регистрируйтесь и отправляйте заявки в боте: {BOT_LINK}"

    success = send_to_telegram(post_text)
    logger.info("✅ Пост опубликован" if success else "❌ Ошибка отправки")
    logger.info("=== ГОТОВО ===")

if __name__ == "__main__":
    asyncio.run(main())
